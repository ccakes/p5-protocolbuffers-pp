=head1 NAME

ProtocolBuffers::Generated::Map - Placeholder for protobuf map entry support

=head1 DESCRIPTION

Map fields are handled directly by L<ProtocolBuffers::PP::Encode> and
L<ProtocolBuffers::PP::Decode> via the C<map_entry> descriptor field. This
module exists as an organizational placeholder.

Map fields are represented as plain Perl hashrefs in message hashes.

=head1 SEE ALSO

L<ProtocolBuffers::PP::Encode>, L<ProtocolBuffers::PP::Decode>

=cut
